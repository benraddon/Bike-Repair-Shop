const griditem1 = document.getElementById('griditem1');

griditem1.addEventListener('click', (event) => {
event.target.style.backgroundColor = 'blue';
event.target.style.color = 'white';
event.target.textContent = `It looks like You've got Cake.🍰`;
});

griditem1.addEventListener('mouseout', (event) => {
event.target.style.backgroundColor = 'lightblue';
event.target.style.color = 'white';
event.target.textContent = `Close your eyes `;
});

